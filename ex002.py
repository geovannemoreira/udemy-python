# Separador sep=''
# Final da linha end='' empede que quebre a linha
# que por utiliza o CRLF \n, \r que seria a quebra de linha

print(12, 1, 34, 5, sep='-#-#-')
print(12, 1, 34, 5)
print('bola', 'cachorro', 'anão', 'bacalhau', end='___miaaaaaaaaaau________')
print('bola', 'cachorro', 'anão', 'bacalhau', sep='----')